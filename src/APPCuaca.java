    private void checkWeather(String city) {
        if (city.isEmpty()) {
        weatherLabel.setText("Nama kota tidak boleh kosong!");
        return;
    }

    try {
        String urlString = BASE_URL + "?q=" + city + "&appid=" + API_KEY + "&units=metric";
        URL url = new URL(urlString);
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        conn.setRequestMethod("GET");

        BufferedReader in = new BufferedReader(new InputStreamReader(conn.getInputStream()));
        StringBuilder response = new StringBuilder();
        String inputLine;

        while ((inputLine = in.readLine()) != null) {
            response.append(inputLine);
        }
        in.close();

        JSONParser parser = new JSONParser();
        JSONObject jsonResponse = (JSONObject) parser.parse(response.toString());
        JSONObject main = (JSONObject) jsonResponse.get("main");
        JSONArray weatherArray = (JSONArray) jsonResponse.get("weather");
        JSONObject weather = (JSONObject) weatherArray.get(0);

        String temperature = main.get("temp").toString();
        String condition = weather.get("description").toString();

        weatherLabel.setText("<html>Cuaca: " + condition + "<br>Suhu: " + temperature + "Â°C</html>");
    } catch (Exception e) {
        weatherLabel.setText("<html>Gagal mengambil data cuaca!<br>Pesan error: " + e.getMessage() + "</html>");
        e.printStackTrace();
    }
}

private void addFavorite(String city) {
    if (!city.isEmpty() && !isCityInFavorites(city)) {
        favoriteModel.addElement(city);
        jComboBox1.setModel(favoriteModel); // Update combo box
    }
}

    private boolean isCityInFavorites(String city) {
        for (int i = 0; i < favoriteModel.getSize(); i++) {
            if (favoriteModel.getElementAt(i).equalsIgnoreCase(city)) {
                return true;
            }
        }
        return false;
    }
